#Task 1: Create a Function to Add Two Numbers
a =  int(input("Enter a: "))
b =  int(input("Enter b: "))
def add(a,b):
    return a + b
result = add(a,b)
print(f"Sum = {result}")

#Create a function named multiply that takes two numbers as parameters and returns their product.

c =  int(input("Enter first number: "))
d =  int(input("Enter another number: "))
def mul(c,d):
    return c * d
product = mul(c,d)
print(f"Product = {product}")